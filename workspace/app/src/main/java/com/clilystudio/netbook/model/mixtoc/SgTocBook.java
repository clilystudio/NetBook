package com.clilystudio.netbook.model.mixtoc;

public class SgTocBook {
    private String author;
    private String book;
    private String md;

    public String getAuthor() {
        return this.author;
    }

    public void setAuthor(String string) {
        this.author = string;
    }

    public String getBook() {
        return this.book;
    }

    public void setBook(String string) {
        this.book = string;
    }

    public String getMd() {
        return this.md;
    }

    public void setMd(String string) {
        this.md = string;
    }
}
